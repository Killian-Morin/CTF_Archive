def decode_slashes(encoded_str):
    # Define mappings for slashes to binary
    binary_map = {'/': '1', '\\': '0'}

    # Split the input string by spaces to separate binary chunks
    groups = encoded_str.split()

    decoded_message = ""

    for group in groups:
        # Replace slashes with binary values
        binary_str = ''.join([binary_map[char] for char in group])
        # Convert binary string to an ASCII character
        decoded_message += chr(int(binary_str, 2))

    return decoded_message

